class Author {

    constructor(id, name, username, email, phone, street, city, zip, company, slogan) {
        this.id = id;
        this.name = name;
        this.username = username;
        this.email = email;
        this.phone = phone;
        this.street = street;
        this.city = city;
        this.zip = zip;
        this.company = company;
        this.slogan = slogan;
    }


}

export default Author;